void draw_char(char c, uint32_t x, uint32_t y, uint32_t fg_color, uint32_t bg_color)
{
    const uint8_t *glyph = font_8x8[(uint8_t)c];

    for (int dy = 0; dy < 8; dy++)
    {
        for (int dx = 0; dx < 8; dx++)
        {
            if (glyph[dy] & (1 << (7 - dx)))
            {
                put_pixel(x + dx, y + dy, fg_color);
            }
            else if (bg_color != 0)
            {
                put_pixel(x + dx, y + dy, bg_color);
            }
        }
    }
}

void draw_string(const char *str, uint32_t x, uint32_t y, uint32_t fg_color, uint32_t bg_color)
{
    uint32_t offset = 0;
    while (*str)
    {
        if (*str == '\n')
        {
            // Handle newline if needed
            str++;
            continue;
        }
        draw_char(*str, x + offset, y, fg_color, bg_color);
        offset += 8;
        str++;
    }
}